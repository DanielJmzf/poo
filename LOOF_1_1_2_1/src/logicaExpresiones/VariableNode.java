package logicaExpresiones;

public class VariableNode extends ASTNode {
    private String nombre;

    public VariableNode(String nombre) {
        this.nombre = nombre;
    }

    public String getNombre() {
        return nombre;
    }

    @Override
    public String toString() {
        return nombre;
    }
}
